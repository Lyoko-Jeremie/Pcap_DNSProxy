//CRC-40 calculate process
uint64_t __fastcall CRC40_Calculate(
	uint64_t CRC, 
	uint8_t *Buffer, 
	const size_t Length)
{
	uint32_t long64_c = 0;
	for (size_t Index = 0;Index < Length;++Index)
	{
		long64_c = 0x00000000000000FFULL & (uint64_t)Buffer[Index];
		CRC = (CRC << 8U) ^ CRC40_Table_Normal[((CRC >> 32U) ^ long64_c) & 0xFF];
	}

	return CRC;
}


