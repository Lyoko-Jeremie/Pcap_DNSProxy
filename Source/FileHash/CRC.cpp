uint16_t __fastcall CRC16_Update_Sick(
	uint16_t CRC, 
	const uint8_t Buffer, 
	const uint8_t PrevByte)
{
	uint16_t short_c = 0x00FF & (uint16_t)Buffer, short_p = (0x00FF & (uint16_t)PrevByte) << 8U;
	if (CRC & 0x8000)
		CRC = (CRC << 1U) ^ 0x8005;
	else 
		CRC = CRC << 1U;
	CRC &= 0xFFFF;
	CRC ^= (short_c | short_p);

	return CRC;
}

